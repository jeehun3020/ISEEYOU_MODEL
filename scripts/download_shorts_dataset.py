#!/usr/bin/env python3
from __future__ import annotations

import argparse
import csv
import hashlib
import json
import re
import sys
from dataclasses import asdict, dataclass
from pathlib import Path
from typing import Any
from urllib.parse import urlparse

ROOT_DIR = Path(__file__).resolve().parents[1]
if str(ROOT_DIR) not in sys.path:
    sys.path.insert(0, str(ROOT_DIR))

from iseeyou.utils.youtube import find_downloaded_video_by_url, validate_youtube_url


VIDEO_EXTENSIONS = {".mp4", ".mkv", ".webm", ".mov", ".avi", ".m4v"}
DEFAULT_FORMAT = "bv*[height<=480][ext=mp4]/b[height<=480][ext=mp4]/b[ext=mp4]/18"
ALLOWED_LABELS = {"real", "generated", "deepfake"}
LABEL_ORDER = ["real", "generated", "deepfake"]


@dataclass
class DownloadStats:
    total_rows: int = 0
    used_rows: int = 0
    downloaded: int = 0
    reused_existing: int = 0
    skipped_invalid_label: int = 0
    skipped_invalid_url: int = 0
    skipped_missing_url: int = 0
    skipped_limit: int = 0
    failed_downloads: int = 0


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Download YouTube Shorts videos from collected CSV files")
    parser.add_argument(
        "--csv",
        action="append",
        default=[],
        help="CSV file path generated by collect_shorts_urls.py (repeatable)",
    )
    parser.add_argument(
        "--output-root",
        type=str,
        default="data/raw/youtube_external",
        help="Root directory for downloaded videos",
    )
    parser.add_argument(
        "--report-path",
        type=str,
        default="data/youtube_external_manifests/shorts_download_report.json",
        help="JSON report path",
    )
    parser.add_argument(
        "--limit-per-label",
        type=int,
        default=0,
        help="Maximum number of downloads per label. 0 means no limit.",
    )
    parser.add_argument(
        "--limit-total",
        type=int,
        default=0,
        help="Maximum number of downloads across all labels. 0 means no limit.",
    )
    parser.add_argument(
        "--max-per-source",
        type=int,
        default=0,
        help="Maximum number of downloads per source/channel. 0 means no limit.",
    )
    parser.add_argument(
        "--format",
        type=str,
        default=DEFAULT_FORMAT,
        help="yt-dlp format string. Defaults to a single mp4-compatible video stream.",
    )
    parser.add_argument(
        "--sleep-interval",
        type=float,
        default=0.0,
        help="yt-dlp sleep interval between requests",
    )
    parser.add_argument(
        "--socket-timeout",
        type=float,
        default=20.0,
        help="yt-dlp socket timeout in seconds",
    )
    parser.add_argument(
        "--retries",
        type=int,
        default=2,
        help="yt-dlp retry count for extractor/download failures",
    )
    parser.add_argument(
        "--overwrite-metadata",
        action="store_true",
        help="Rewrite sidecar metadata JSON even if the video already exists.",
    )
    parser.add_argument(
        "--fallback-label",
        type=str,
        default="",
        help="Optional label to assign when CSV rows have an empty/invalid suggested_label.",
    )
    parser.add_argument(
        "--verbose",
        action="store_true",
        help="Show yt-dlp logs",
    )
    return parser.parse_args()


def read_csv_rows(path: Path) -> list[dict[str, str]]:
    with path.open("r", encoding="utf-8", newline="") as f:
        return list(csv.DictReader(f))


def load_candidate_rows(
    csv_paths: list[Path],
    stats: DownloadStats,
    fallback_label: str = "",
) -> list[tuple[Path, dict[str, str]]]:
    candidates: list[tuple[Path, dict[str, str]]] = []
    normalized_fallback = fallback_label.strip().lower()

    for csv_path in csv_paths:
        rows = read_csv_rows(csv_path)
        stats.total_rows += len(rows)

        for raw_row in rows:
            row = dict(raw_row)
            label = (row.get("suggested_label") or "").strip().lower()
            if label not in ALLOWED_LABELS and normalized_fallback:
                label = normalized_fallback
            if not label:
                stats.skipped_invalid_label += 1
                continue
            if label not in ALLOWED_LABELS and label != normalized_fallback:
                stats.skipped_invalid_label += 1
                continue

            url_raw = (row.get("shorts_url") or row.get("webpage_url") or "").strip()
            if not url_raw:
                stats.skipped_missing_url += 1
                continue

            video_id = (row.get("video_id") or "").strip()
            if not video_id:
                stats.skipped_invalid_url += 1
                continue

            try:
                resolved_url = validate_youtube_url(url_raw)
            except ValueError:
                stats.skipped_invalid_url += 1
                continue

            row["resolved_url"] = resolved_url
            row["resolved_label"] = label
            candidates.append((csv_path, row))

    return candidates


def select_download_queue(
    candidates: list[tuple[Path, dict[str, str]]],
    limit_per_label: int,
    limit_total: int,
    max_per_source: int,
) -> list[tuple[Path, dict[str, str]]]:
    grouped: dict[str, dict[str, list[tuple[Path, dict[str, str]]]]] = {}

    for item in candidates:
        _, row = item
        label = row["resolved_label"]
        source = (row.get("source_value") or "").strip() or "unknown_source"
        grouped.setdefault(label, {}).setdefault(source, []).append(item)

    selected: list[tuple[Path, dict[str, str]]] = []

    ordered_labels = [label for label in LABEL_ORDER if label in grouped]
    ordered_labels.extend(sorted(label for label in grouped if label not in LABEL_ORDER))

    for label in ordered_labels:
        source_buckets = grouped.get(label, {})
        if not source_buckets:
            continue

        source_keys = sorted(source_buckets.keys())
        source_counts = {key: 0 for key in source_keys}
        label_count = 0

        while True:
            progressed = False
            for source_key in source_keys:
                if limit_total > 0 and len(selected) >= limit_total:
                    return selected
                if limit_per_label > 0 and label_count >= limit_per_label:
                    break
                if max_per_source > 0 and source_counts[source_key] >= max_per_source:
                    continue

                bucket = source_buckets[source_key]
                if not bucket:
                    continue

                selected.append(bucket.pop(0))
                source_counts[source_key] += 1
                label_count += 1
                progressed = True

            if not progressed or (limit_per_label > 0 and label_count >= limit_per_label):
                break

    return selected


def sanitize_slug(value: str) -> str:
    cleaned = value.strip().lower()
    cleaned = re.sub(r"[^a-z0-9._-]+", "_", cleaned)
    cleaned = cleaned.strip("._-")
    return cleaned[:80]


def infer_channel_slug(row: dict[str, str]) -> str:
    source_value = (row.get("source_value") or "").strip()
    uploader = (row.get("uploader") or "").strip()
    channel_id = (row.get("channel_id") or "").strip()

    handle = ""
    if source_value:
        parsed = urlparse(source_value)
        path_parts = [p for p in parsed.path.split("/") if p]
        if path_parts:
            handle = path_parts[0].lstrip("@")

    base = sanitize_slug(handle) or sanitize_slug(uploader) or sanitize_slug(channel_id)
    if not base:
        digest = hashlib.md5(source_value.encode("utf-8")).hexdigest()[:10]
        return f"channel_{digest}"

    if source_value:
        digest = hashlib.md5(source_value.encode("utf-8")).hexdigest()[:6]
        return f"{base}_{digest}"
    return base


def find_existing_download(channel_dir: Path, video_id: str) -> Path | None:
    matches = [
        p
        for p in channel_dir.glob(f"{video_id}*")
        if p.is_file() and p.suffix.lower() in VIDEO_EXTENSIONS
    ]
    if not matches:
        return None
    return max(matches, key=lambda p: p.stat().st_mtime)


def write_sidecar_metadata(
    sidecar_path: Path,
    row: dict[str, str],
    video_path: Path,
) -> None:
    payload = {
        "video_path": str(video_path),
        "row": row,
    }
    sidecar_path.parent.mkdir(parents=True, exist_ok=True)
    sidecar_path.write_text(json.dumps(payload, indent=2, ensure_ascii=False), encoding="utf-8")


def build_ydl_opts(
    outtmpl: str,
    fmt: str,
    sleep_interval: float,
    socket_timeout: float,
    retries: int,
    verbose: bool,
) -> dict[str, Any]:
    return {
        "format": fmt,
        "outtmpl": outtmpl,
        "noplaylist": True,
        "quiet": not verbose,
        "no_warnings": not verbose,
        "ignoreerrors": False,
        "sleep_interval": max(0.0, sleep_interval),
        "socket_timeout": max(1.0, socket_timeout),
        "retries": max(0, retries),
        "fragment_retries": max(0, retries),
        "extractor_retries": max(0, retries),
    }


def main() -> None:
    args = parse_args()
    csv_paths = [Path(p) for p in args.csv]
    if not csv_paths:
        raise SystemExit("[ERROR] Provide at least one --csv file.")

    for csv_path in csv_paths:
        if not csv_path.exists():
            raise SystemExit(f"[ERROR] CSV not found: {csv_path}")

    try:
        from yt_dlp import YoutubeDL  # type: ignore
    except ModuleNotFoundError as exc:
        raise SystemExit("[ERROR] yt-dlp is required. Install with: pip install yt-dlp") from exc

    output_root = Path(args.output_root)
    output_root.mkdir(parents=True, exist_ok=True)
    stats = DownloadStats()
    report_rows: list[dict[str, str]] = []

    candidates = load_candidate_rows(
        csv_paths,
        stats,
        fallback_label=args.fallback_label,
    )
    labels_in_use = sorted({row["resolved_label"] for _, row in candidates})
    downloads_per_label = {label: 0 for label in labels_in_use}
    queue = select_download_queue(
        candidates=candidates,
        limit_per_label=max(0, args.limit_per_label),
        limit_total=max(0, args.limit_total),
        max_per_source=max(0, args.max_per_source),
    )
    stats.skipped_limit += max(0, len(candidates) - len(queue))

    for csv_path, row in queue:
        label = row["resolved_label"]
        url = row["resolved_url"]
        video_id = (row.get("video_id") or "").strip()

        channel_slug = infer_channel_slug(row)
        channel_dir = output_root / label / channel_slug
        channel_dir.mkdir(parents=True, exist_ok=True)
        sidecar_path = channel_dir / f"{video_id}.json"

        existing = find_existing_download(channel_dir, video_id)
        if existing is not None:
            stats.used_rows += 1
            stats.reused_existing += 1
            downloads_per_label[label] += 1
            if args.overwrite_metadata or not sidecar_path.exists():
                write_sidecar_metadata(sidecar_path, row, existing)
            report_rows.append(
                {
                    "status": "existing",
                    "label": label,
                    "csv_path": str(csv_path),
                    "url": url,
                    "video_path": str(existing),
                }
            )
            continue

        outtmpl = str(channel_dir / f"{video_id}.%(ext)s")
        ydl_opts = build_ydl_opts(
            outtmpl=outtmpl,
            fmt=args.format,
            sleep_interval=args.sleep_interval,
            socket_timeout=args.socket_timeout,
            retries=args.retries,
            verbose=args.verbose,
        )

        try:
            with YoutubeDL(ydl_opts) as ydl:
                ydl.extract_info(url, download=True)
        except Exception:
            stats.failed_downloads += 1
            report_rows.append(
                {
                    "status": "failed",
                    "label": label,
                    "csv_path": str(csv_path),
                    "url": url,
                    "video_path": "",
                }
            )
            continue

        matched = find_existing_download(channel_dir, video_id)
        if matched is None:
            matched = find_downloaded_video_by_url(url, channel_dir, VIDEO_EXTENSIONS)

        if matched is None:
            stats.failed_downloads += 1
            report_rows.append(
                {
                    "status": "missing_after_download",
                    "label": label,
                    "csv_path": str(csv_path),
                    "url": url,
                    "video_path": "",
                }
            )
            continue

        stats.used_rows += 1
        stats.downloaded += 1
        downloads_per_label[label] += 1
        write_sidecar_metadata(sidecar_path, row, matched)
        report_rows.append(
            {
                "status": "downloaded",
                "label": label,
                "csv_path": str(csv_path),
                "url": url,
                "video_path": str(matched),
            }
        )

    report_path = Path(args.report_path)
    report_path.parent.mkdir(parents=True, exist_ok=True)
    report = {
        "stats": asdict(stats),
        "downloads_per_label": downloads_per_label,
        "csv_paths": [str(p) for p in csv_paths],
        "output_root": str(output_root),
        "rows": report_rows,
    }
    report_path.write_text(json.dumps(report, indent=2, ensure_ascii=False), encoding="utf-8")

    print("[INFO] shorts download finished")
    print(f"[INFO] output_root={output_root}")
    print(f"[INFO] report_path={report_path}")
    print(
        "[INFO] stats: "
        f"total_rows={stats.total_rows}, used_rows={stats.used_rows}, "
        f"downloaded={stats.downloaded}, reused={stats.reused_existing}, "
        f"failed={stats.failed_downloads}, invalid_label={stats.skipped_invalid_label}, "
        f"invalid_url={stats.skipped_invalid_url}, missing_url={stats.skipped_missing_url}, "
        f"limit_skip={stats.skipped_limit}"
    )
    print(f"[INFO] downloads_per_label={downloads_per_label}")


if __name__ == "__main__":
    main()
